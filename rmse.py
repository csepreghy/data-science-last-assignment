import numpy as np

# input: 1) f: the predicted values of the dependent output variable, 
#           an N-dimensional vector, as a numpy array
#        2) t: the ground truth values of dependent output variable, 
#           an N-dimensional vector, as a numpy array
#
# output: 1) the root mean square error (rmse) as a 1 x 1 numpy array

def rmse(f, t):
  n = len(f)
  return np.linalg.norm(f - t) / np.sqrt(n)
